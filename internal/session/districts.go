package session

// DistrictByName returns the district with the given name, or false if unknown.
func DistrictByName(name string) (District, bool) {
	for _, d := range Districts {
		if d.Name == name {
			return d, true
		}
	}
	return District{}, false
}

// StationsForDistricts returns the deduplicated union of stations belonging
// to any of the given district names.
func StationsForDistricts(districtNames []string) []string {
	seen := make(map[string]bool)
	var stations []string
	for _, name := range districtNames {
		district, ok := DistrictByName(name)
		if !ok {
			continue
		}
		for _, s := range district.Stations {
			if !seen[s] {
				seen[s] = true
				stations = append(stations, s)
			}
		}
	}
	return stations
}

