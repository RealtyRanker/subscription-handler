package session

// OkrugByName returns the okrug with the given name, or false if unknown.
func OkrugByName(name string) (Okrug, bool) {
	for _, o := range Okrugs {
		if o.Name == name {
			return o, true
		}
	}
	return Okrug{}, false
}

// StationsForOkrugs returns the deduplicated union of stations belonging to
// any of the given okrug names.
func StationsForOkrugs(okrugNames []string) []string {
	seen := make(map[string]bool)
	var stations []string
	for _, name := range okrugNames {
		okrug, ok := OkrugByName(name)
		if !ok {
			continue
		}
		for _, s := range okrug.Stations {
			if !seen[s] {
				seen[s] = true
				stations = append(stations, s)
			}
		}
	}
	return stations
}

