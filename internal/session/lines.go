package session

// LineByNumber returns the line with the given Number (e.g. "1", "D2"), or
// false if unknown.
func LineByNumber(number string) (Line, bool) {
	for _, l := range Lines {
		if l.Number == number {
			return l, true
		}
	}
	return Line{}, false
}

// StationsForLines returns the deduplicated union of stations belonging to
// any of the given line numbers.
func StationsForLines(numbers []string) []string {
	seen := make(map[string]bool)
	var stations []string
	for _, number := range numbers {
		line, ok := LineByNumber(number)
		if !ok {
			continue
		}
		for _, s := range line.Stations {
			if !seen[s] {
				seen[s] = true
				stations = append(stations, s)
			}
		}
	}
	return stations
}

// Line describes one Moscow metro line (including MCC/MCD diameters) and the
// canonical subway station names (matching realty-parser's scoring.subways,
// the source of truth for station spelling) that belong to it.
type Line struct {
	Emoji    string
	Name     string
	Number   string
	Stations []string
}

// Label returns the display label for the line, e.g. "🔴 Сокольническая (1)"
// or "Некрасовская (15)" when there's no emoji.
func (l Line) Label() string {
	if l.Emoji != "" {
		return l.Emoji + " " + l.Name + " (" + l.Number + ")"
	}
	return l.Name + " (" + l.Number + ")"
}

